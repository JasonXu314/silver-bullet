size_t accept[8] = {0, 0, 3, 2, 3, 0, 0, 1};

string ruleNames[] = {"", "primitive::pattern", "primitive::ws", "primitive::raw"};

struct TrapRecord {
	size_t state;
	size_t pos;
};

string fix(string str);

bool operator<(const TrapRecord& a, const TrapRecord& b);

int main() {
	string token;
	stack<size_t> trail;
	set<TrapRecord> hopeless;
	size_t state = 1, pos = 0;

	while (cin && cin.peek() != EOF) {
		unsigned char c = cin.get();

		size_t nextState = ::next[state * 256 + c];

		token.push_back(c);
		if (nextState == 0 || hopeless.count({nextState, pos})) {
			if (!trail.empty()) {
				do {
					cin.putback(token.back());

					if (trail.empty()) break;

					hopeless.emplace(state, pos);
					state = trail.top();
					trail.pop();
					token.pop_back();
				} while (!accept[state]);

				if (accept[state]) {
					cout << "accept: " << fix(token) << " => " << ruleNames[accept[state]] << endl;
					state = 1;
					pos = 0;
					token.clear();
					while (!trail.empty()) trail.pop();
					hopeless.clear();
				} else {
					cout << "bad trailkeeping" << endl;
					state = 1;
					pos = 0;
					token.clear();
					hopeless.clear();
				}
			} else {
				cout << "reject" << endl;
				state = 1;
				pos = 0;
				token.clear();
				while (!trail.empty()) trail.pop();
				hopeless.clear();
			}
		} else {
			state = nextState;
			pos++;

			trail.push(state);
		}
	}

	if (accept[state]) {
		cout << "accept: " << fix(token) << " => " << ruleNames[accept[state]] << endl;
	} else {
		cout << "reject" << endl;
	}

	return 0;
}

string fix(string str) {
	for (size_t i = 0; i < str.length(); i++) {
		switch (str[i]) {
			case '\n':
				str[i] = '\\';
				str.insert(str.begin() + i + 1, 'n');
				break;
			case '\r':
				str[i] = '\\';
				str.insert(str.begin() + i + 1, 'r');
				break;
		}
	}

	return str;
}

bool operator<(const TrapRecord& a, const TrapRecord& b) {
	return a.state < b.state || a.pos < b.pos;
}