int main() {
	size_t state = 1, lastAccept = 0;

	while (cin && cin.peek() != EOF) {
		unsigned char c = cin.get();

		size_t nextState = ::next[state * 256 + c];

		// TODO: rudimentary backtracking
		if (nextState == 0) {
			if (lastAccept) {
				cin.putback(c);
				cout << "accept: " << accept[lastAccept] << endl;
			} else {
				cout << "reject" << endl;
			}
		} else {
			state = nextState;

			if (accept[state]) {
				lastAccept = state;
			}
		}
	}

	if (accept[state]) {
		cout << "accept: " << accept[state] << endl;
	} else {
		cout << "reject" << endl;
	}

	return 0;
}